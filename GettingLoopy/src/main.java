public class main {
    public static void main(String[] args) {
        // 0 to 30 by 1
        for (int num=0; num <= 30; num++)
        {
            System.out.println(num);
        }

        // 30 to 0 by 1
        for (int num=30; num >= 00; num--)
        {
            System.out.println(num);
        }

        // 0 to 18 by 3
        for (int num=0; num <= 18; num = num + 3)
        {
            System.out.println(num);
        }

        // 10 to 0 by 2
        for (int num=10; num >= 0; num = num - 2)
        {
            System.out.println(num);
        }
    }

}
